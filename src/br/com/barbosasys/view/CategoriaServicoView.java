    private void btnExcluirCategoriaServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirCategoriaServicoActionPerformed
        // TODO add your handling code here:
        if (testarSelecao() == true) {
            int linha = tblCategoriaServico.getSelectedRow();
            String descricao = (String) tblCategoriaServico.getValueAt(linha, 1);
            int codigo = (int) tblCategoriaServico.getValueAt(linha, 0);

            // Questiona se realmente deseja excluir
            int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja"
                    + " excluir o registro:\n" + descricao + "?", "Atenção", JOptionPane.YES_NO_OPTION);
            //se sim exclui, se não não faz nada
            if (opcao == JOptionPane.OK_OPTION) {
                if (categoriaServicoController.excluirCategoriaServicoController(codigo)) {
                    JOptionPane.showMessageDialog(this, "Registro excluído com sucesso!");
                    incluirCategoriaServico();
                    carregarCategoriaServico();
                    jTabbedPaneCategoriaServico.setSelectedIndex(0);

                } else {
                    JOptionPane.showMessageDialog(this, "Erro!", "ERRO", JOptionPane.ERROR_MESSAGE);

                }
            }
        }
    }//GEN-LAST:event_btnExcluirCategoriaServicoActionPerformed

