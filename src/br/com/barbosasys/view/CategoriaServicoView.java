    private void btnSalvarCategoriaServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCategoriaServicoActionPerformed
        // TODO add your handling code here:
        if (txtDescricaoCategoriaServico.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "É necessário atribuir uma descricao a categoria do serviço");
            txtDescricaoCategoriaServico.grabFocus();
        } else if (tipoCadastro.equals("novo")) {
            salvarCategoriaServico();

        } else if (tipoCadastro.equals("alteracao")) {
            alterarCategoriaServico();
        }
    }//GEN-LAST:event_btnSalvarCategoriaServicoActionPerformed

    private void carregarCategoriaServico() {
        listaCategoriaServico = categoriaServicoController.getListaCategoriaServicoController();
        DefaultTableModel modelo = (DefaultTableModel) tblCategoriaServico.getModel();
        modelo.setNumRows(0);
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listaCategoriaServico.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaCategoriaServico.get(i).getCodigo(),
                listaCategoriaServico.get(i).getDescricao()
            });
        }
    }

    private boolean recuperaCategoriaServico() {
        //Armazena a linha selecionada
        int linhaTabela = this.tblCategoriaServico.getSelectedRow();
        //Captura valor código da unidade da linha selecionada
        int codigo = (Integer) tblCategoriaServico.getValueAt(linhaTabela, 0);
        jTabbedPaneCategoriaServico.setSelectedIndex(1);

        try {
            categoriaServico = categoriaServicoController.getCategoriaServicoController(codigo);
            this.txtCodCategoriaServico.setText(String.valueOf(categoriaServico.getCodigo()));
            this.txtDescricaoCategoriaServico.setText(categoriaServico.getDescricao());

            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

    private boolean testarSelecao() {
        int selecao = tblCategoriaServico.getSelectedRow();
        if (selecao == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para realizar essa operação.");
            return false;
        }
        return true;
    }

    private void incluirCategoriaServico() {
        jTabbedPaneCategoriaServico.setSelectedIndex(1);
        txtCodCategoriaServico.setText("novo");
        txtDescricaoCategoriaServico.setText(null);
        tipoCadastro = "novo";
    }

    private boolean alterarCategoriaServico() {
        categoriaServico.setCodigo((Integer.parseInt(this.txtCodCategoriaServico.getText())));
        categoriaServico.setDescricao(this.txtDescricaoCategoriaServico.getText());

        if (categoriaServicoController.atualizarCategoriaServicoController(categoriaServico)) {
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
            this.incluirCategoriaServico();
            this.carregarCategoriaServico();
            jTabbedPaneCategoriaServico.setSelectedIndex(0);

            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean salvarCategoriaServico() {
        categoriaServico.setDescricao(this.txtDescricaoCategoriaServico.getText());

        if (categoriaServicoController.salvarCategoriaServicoController(categoriaServico) > 0) {
            JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");
            this.incluirCategoriaServico();
            this.carregarCategoriaServico();
            jTabbedPaneCategoriaServico.setSelectedIndex(0);
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

