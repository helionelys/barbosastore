    private void carregarProdutos() {
        listaProduto = produtoController.getListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
        modelo.setNumRows(0);
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listaProduto.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaProduto.get(i).getCodProduto(),
                listaProduto.get(i).getDescricao(),
                listaProduto.get(i).getDescricaoUnidadeMedida()
            });
        }
        this.camposDesativados();
    }

    private boolean recuperarProduto() {
        //Armazena a linha selecionada
        int linhaTabela = this.tblProdutos.getSelectedRow();

        //Captura valor código do cliente da linha selecionada
        int codigo = (Integer) tblProdutos.getValueAt(linhaTabela, 0);

        jTabbedPaneProduto.setSelectedIndex(1);
//        cbUnidadeProduto.removeAll();
//        cbCategoriaProduto.removeAll();
//        cbFornecedorProduto.removeAll();
        try {
            // retorna os dados do banco
            produto = produtoController.getProdutoController(codigo);
            this.txtCodigoProduto.setText(String.valueOf(produto.getCodProduto()));
            System.out.println(codigo);

            String dataRetorno = produto.getDataCadastramento();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            DateTimeFormatter formatter2 = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate localDate = LocalDate.parse(dataRetorno, formatter);
            String dataCadastramentoTela = localDate.format(formatter2);
            this.txtDataCadastroProduto.setText(dataCadastramentoTela);

            this.txtDescricaoProduto.setText(produto.getDescricao());
            this.cbUnidadeProduto.setSelectedItem(unidadeMedidaController.getUnidadeMedidaController(produto.getCodUnidadeMedida()).getDescricao());
            this.cbCategoriaProduto.setSelectedItem(categoriaProdutoController.getCategoriaProdutoController(produto.getCodCategoriaProduto()).getDescricao());

            Double valorRetorno = produto.getValor();
            DecimalFormat df = new DecimalFormat("#,##0.00");
            String valorTela = df.format(valorRetorno);
            this.txtValorProduto.setText(valorTela);
            
            Double valorRetorno2 = produto.getValorCompra();
            DecimalFormat df2 = new DecimalFormat("#,##0.00");
            String valorCompraTela = df2.format(valorRetorno2);
            this.txtValorProdutoCompra.setText(valorCompraTela);

            //System.out.println(valorTela);
            this.cbFornecedorProduto.setSelectedItem(fornecedorController.getFornecedorController(produto.getCodFornecedor()).getNomeRazaoSocial());
            this.txtFabricanteProduto.setText(produto.getFabricante());
            this.txtObservacaoProduto.setText(produto.getObservacao());
            this.txtQtdEstoqueProduto.setText(String.valueOf(produto.getQuantidade()));
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

