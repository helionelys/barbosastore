    private void excluirProduto() {
        if (testarSelecao() == true) {
            int linha = tblProdutos.getSelectedRow();
            String nome = (String) tblProdutos.getValueAt(linha, 1);
            int codigo = (Integer) tblProdutos.getValueAt(linha, 0);

            // Questiona a exclusão
            int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja"
                    + " excluir o registro \nNome: "
                    + nome + " ?", "Atenção", JOptionPane.YES_NO_OPTION);
            //se sim exclui, se não não faz nada    
            if (opcao == JOptionPane.OK_OPTION) {
                if (produtoController.excluirProdutoController(codigo)) {
                    JOptionPane.showMessageDialog(this, "Registro excluído com suscesso!");
                    carregarProdutos();
                    //incluirProduto();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao e os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);

                }
            }
        }
    }

    private boolean alterarProduto() {
        produto.setCodProduto((Integer.parseInt(this.txtCodigoProduto.getText())));
        produto.setDescricao(this.txtDescricaoProduto.getText());
        produto.setCodCategoriaProduto(categoriaProdutoController.getCategoriaProdutoController(this.cbCategoriaProduto.getSelectedItem().toString()).getCodigo());
        produto.setCodUnidadeMedida(unidadeMedidaController.getUnidadeMedidaController(this.cbUnidadeProduto.getSelectedItem().toString()).getCodigo());

        System.out.println(categoriaProdutoController.getCategoriaProdutoController(this.cbCategoriaProduto.getSelectedItem().toString()).getCodigo());
        System.out.println(unidadeMedidaController.getUnidadeMedidaController(this.cbCategoriaProduto.getSelectedItem().toString()).getCodigo());

        String valorProduto = this.txtValorProduto.getText();
        String resultadoFormatado = valorProduto.replace(".", "");
        String resultadoFormatado2 = resultadoFormatado.replace(",", ".");
        Double valorProdutoBancoDados = Double.parseDouble(resultadoFormatado2);
        produto.setValor(valorProdutoBancoDados);
        
        String valorProdutoCompra = this.txtValorProdutoCompra.getText();
        String resultadoFormatado3 = valorProdutoCompra.replace(".", "");
        String resultadoFormatado4 = resultadoFormatado3.replace(",", ".");
        Double valorProdutoCompraBancoDados = Double.parseDouble(resultadoFormatado4);
        produto.setValorCompra(valorProdutoCompraBancoDados);
        
        produto.setCodFornecedor(fornecedorController.getFornecedorController(this.cbFornecedorProduto.getSelectedItem().toString()).getCodigo());
        System.out.println(fornecedorController.getFornecedorController(this.cbFornecedorProduto.getSelectedItem().toString()).getCodigo());
        produto.setFabricante(this.txtFabricanteProduto.getText());
        produto.setObservacao(this.txtObservacaoProduto.getText());

        if (produtoController.atualizarProdutoController(produto)) {
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
            this.incluirProduto();
            this.carregarProdutos();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

