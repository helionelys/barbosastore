    private void carregarFuncionario() {
        listaFuncionario = funcionarioController.getListaFuncionarioController();
        DefaultTableModel modelo = (DefaultTableModel) tblFuncionarios.getModel();
        modelo.setNumRows(0);
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listaFuncionario.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaFuncionario.get(i).getCodigo(),
                listaFuncionario.get(i).getNomeRazaoSocial(),
                listaFuncionario.get(i).getCpfCnpj(),
                listaFuncionario.get(i).getNomeMae(),
                listaFuncionario.get(i).getEmail()
            });

        }
        this.camposDesativados();
    }

    private boolean recuperarFuncionario() {
        //Armazena a linha selecionada
        int linhaTabela = this.tblFuncionarios.getSelectedRow();

        //Captura valor código do funcionario da linha selecionada
        int codigo = (Integer) tblFuncionarios.getValueAt(linhaTabela, 0);
        System.out.println(codigo);
        jTabbedPaneFuncionario.setSelectedIndex(1);
        camposAtivados();

        try {
            // retorna os dados do banco
            funcionario = funcionarioController.getFuncionarioController(codigo);
            //this.txtCodigoFuncionario.setText(Integer.parseInt((funcionario.getCodigo())));
            this.txtCodigoFuncionario.setText(String.valueOf(funcionario.getCodigo()));
            this.txtNomeFuncionario.setText(funcionario.getNomeRazaoSocial());

            String dataRetorno = funcionario.getDataNascimento();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            DateTimeFormatter formatter2 = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate localDate = LocalDate.parse(dataRetorno, formatter);
            String dataAniversarioTelaFuncionario = localDate.format(formatter2);
            this.txtDataNascimentoFuncionario.setText(dataAniversarioTelaFuncionario);
            this.cbSexoFuncionario.setSelectedItem(funcionario.getSexo());
            this.txtCpfFuncionario.setText(funcionario.getCpfCnpj());
            this.txtRgFuncionario.setText(funcionario.getRgInscricaoEstadual());
            this.txtRgOrgaoExpedidorFuncionario.setText(funcionario.getRgOrgaoExpedidor());
            this.txtMaeFuncionario.setText(funcionario.getNomeMae());
            this.txtPaiFuncionario.setText(funcionario.getNomePai());
            this.cbEscolaridadeFuncionario.setSelectedItem(funcionario.getEscolaridade());
            this.cbEstadoCivelFuncionario.setSelectedItem(funcionario.getEstadoCivil());

            Double salarioRetorno = funcionario.getSalario();
            DecimalFormat df = new DecimalFormat("#,##0.00");
            String salarioTela = df.format(salarioRetorno);
            this.txtSalarioFuncionario.setText(salarioTela);
            //System.out.println(salarioTela);

            // this.txtSalarioFuncionario.setText(String.valueOf(funcionario.getSalario()));
            this.txtFuncaoFuncionario.setText(funcionario.getFuncao());
            this.txtDepartamentoFuncionario.setText(funcionario.getDepartamento());
            this.txtCepFuncionario.setText(funcionario.getCep());
            this.txtCidadeFuncionario.setText(funcionario.getCidade());
            this.cbUfFuncionario.setSelectedItem(funcionario.getUf());
            this.txtLogradouroFuncionario.setText(funcionario.getLogradouro());
            this.txtNumeroFuncionario.setText(funcionario.getNumero());
            this.txtBairroFuncionario.setText(funcionario.getBairro());
            this.txtComplementoFuncionario.setText(funcionario.getComplemento());
            this.txtCelularFuncionario.setText(funcionario.getCelular());
            this.txtEmailFuncionario.setText(funcionario.getEmail());

            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

