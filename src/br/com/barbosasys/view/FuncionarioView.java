    private void btnSalvarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarFuncionarioActionPerformed
        // TODO add your handling code here:
        if (txtNomeFuncionario.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "É necessário atribuir um nome ao funcionario");
            txtNomeFuncionario.grabFocus();
        } else if (tipoCadastro.equals("novo")) {
            salvarFuncionario();
            carregarFuncionario();
            int opcao = JOptionPane.showConfirmDialog(this, "Deseje realiza Inclui um novo funcionário ?\n"
                    + "", "Confirma", JOptionPane.YES_NO_OPTION);
            if (opcao == JOptionPane.OK_OPTION) {
                this.incluirFuncionario();
                this.camposAtivados();
            } else {
                
                jTabbedPaneFuncionario.setSelectedIndex(0);
            }
        } else if (tipoCadastro.equals("alteracao")) {
            alteraFuncionario();
            carregarFuncionario();
            incluirFuncionario();
             jTabbedPaneFuncionario.setSelectedIndex(0);
        }

    }//GEN-LAST:event_btnSalvarFuncionarioActionPerformed

